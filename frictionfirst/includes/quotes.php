function getTodaysQuote() {
    $quotes = getQuotes();
    $dayOfYear = (int)date('z'); // 0-365
    $index = $dayOfYear % count($quotes);
    return $quotes[$index];
}

function getQuoteByIndex($index) {
    $quotes = getQuotes();
    return $quotes[$index % count($quotes)];
}
